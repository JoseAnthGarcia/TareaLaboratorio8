package beans;

public class BodegasAdminBean {

    private int rucBodega;
    private String nombreBodega;
    private String estadoBodega;

    public int getRucBodega() {
        return rucBodega;
    }

    public void setRucBodega(int rucBodega) {
        this.rucBodega = rucBodega;
    }

    public String getNombreBodega() {
        return nombreBodega;
    }

    public void setNombreBodega(String nombreBodega) {
        this.nombreBodega = nombreBodega;
    }

    public String getEstadoBodega() {
        return estadoBodega;
    }

    public void setEstadoBodega(String estadoBodega) {
        this.estadoBodega = estadoBodega;
    }

}
